CREATE DATABASE "INVENTORY_APP";

CREATE TABLE "R_STATE" (
    "SYS_ID_R_STATE" varchar (40) PRIMARY KEY,
    "NAME_R_STATE" varchar (40)
);

CREATE TABLE "TR_CATEGORY" (
    "SYS_ID_TR_CATEGORY" varchar (40) PRIMARY KEY,
    "NAME_TR_CATEGORY" varchar (40)
);

CREATE TABLE "TR_BARANG" (
    "SYS_ID_TR_BARANG" varchar (40) PRIMARY KEY,
    "NAME_TR_BARANG" varchar (40),
    "CATEGORY" varchar (40),
    "MERK" varchar (40),
    "TIPE" varchar (40),
    "SERIAL_NUMBER" varchar (40),
    "TANGGAL_PEMBELIAN" timestamp without time zone,
    "HARGA" NUMERIC(20,2),
    "KETERANGAN" text,
    "STATE" varchar (40)
);

