SELECT 
"SYS_ID_TR_BARANG", "NAME_TR_BARANG", "NUMBER", "CATEGORY", "MERK", "TIPE", "SERIAL_NUMBER", "TANGGAL_PEMBELIAN", "HARGA", "KETERANGAN", "STATE"
FROM "TR_BARANG"



SELECT 
    SUM(1) AS "TOTAL BARANG",

    SUM(CASE WHEN "STATE" = (
        SELECT "SYS_ID_R_STATE" FROM "R_STATE" WHERE "NAME_R_STATE" = 'Tersedia'
    ) THEN 1 ELSE 0 END) AS "BARANG_TERSEDIA",

    SUM(CASE WHEN "STATE" = (
        SELECT "SYS_ID_R_STATE" FROM "R_STATE" WHERE "NAME_R_STATE" = 'Dipinjam'
    ) THEN 1 ELSE 0 END) AS "BARANG_DIPINJAM",

    SUM(CASE WHEN "STATE" = (
        SELECT "SYS_ID_R_STATE" FROM "R_STATE" WHERE "NAME_R_STATE" = 'Rusak'
    ) THEN 1 ELSE 0 END) AS "BARANG_RUSAK"

FROM "TR_BARANG"



SELECT 
    g."NAME_TR_CATEGORY",
    (SELECT COUNT(*) FROM "TR_BARANG" b WHERE b."CATEGORY" = g."SYS_ID_TR_CATEGORY" ) AS "TOTAL_BARANG"
FROM "TR_CATEGORY" g;






SELECT 
b."SYS_ID_TR_BARANG", b."NAME_TR_BARANG", b."NUMBER", b."CATEGORY" AS "CATEGORY_ID", tc."NAME_TR_CATEGORY" AS "CATEGORY_NAME", b."MERK", b."TIPE", b."SERIAL_NUMBER", b."TANGGAL_PEMBELIAN", b."HARGA", b."KETERANGAN", b."STATE" AS "STATE_ID", s."NAME_R_STATE" AS "STATE_NAME"
FROM "TR_BARANG" b
LEFT JOIN "R_STATE" s ON s."SYS_ID_R_STATE" = b."STATE"
LEFT JOIN "TR_CATEGORY" tc ON tc."SYS_ID_TR_CATEGORY" = b."CATEGORY";

